package edu.snu.tempest.operator.window.time.sts;

/**
 * STSWindowOutput generated by sts operator.
 */
public final class STSWindowOutput<V> {
  /**
   * An output.
   */
  public final V output;

  /**
   * Start time of the output.
   */
  public final long startTime;

  /**
   * End time of the output.
   */
  public final long endTime;

  /**
   * Is this output fully processed or not.
   */
  public final boolean fullyProcessed;

  /**
   * STSWindowOutput.
   * @param output an output
   * @param startTime a start time of the output
   * @param endTime an end time of the output
   * @param fullyProcessed is this output fully processed or not
   */
  public STSWindowOutput(final V output,
                         final long startTime,
                         final long endTime,
                         final boolean fullyProcessed) {
    this.output = output;
    this.startTime = startTime;
    this.endTime = endTime;
    this.fullyProcessed = fullyProcessed;
  }

  @Override
  public String toString() {
    StringBuilder sb = new StringBuilder();
    sb.append("range: [");
    sb.append(startTime);
    sb.append("-");
    sb.append(endTime);
    sb.append("], output:");
    sb.append(output);
    return sb.toString();
  }
}
